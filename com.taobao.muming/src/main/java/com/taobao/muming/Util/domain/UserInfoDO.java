package com.taobao.muming.Util.domain;

public class UserInfoDO {
	private String activityAwardPrice;
	private String name;
	private String phoneNumber;
	private String postcode;
	private String detailedAddress;
	private String sequenceno;
	private String luckytime;
	private String iteminfo;
	
	public String getActivityAwardPrice() {
		return activityAwardPrice;
	}
	public void setActivityAwardPrice(String activityAwardPrice) {
		this.activityAwardPrice = activityAwardPrice;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	public String getDetailedAddress() {
		return detailedAddress;
	}
	public void setDetailedAddress(String detailedAddress) {
		this.detailedAddress = detailedAddress;
	}
	public String getSequenceno() {
		return sequenceno;
	}
	public void setSequenceno(String sequenceno) {
		this.sequenceno = sequenceno;
	}
	public String getLuckytime() {
		return luckytime;
	}
	public void setLuckytime(String luckytime) {
		this.luckytime = luckytime;
	}
	public String getIteminfo() {
		return iteminfo;
	}
	public void setIteminfo(String iteminfo) {
		this.iteminfo = iteminfo;
	}
	
}
